# Desafio 005 - Faça um programa que leia um numero inteiro e mostre seu antecessor e seu sucessor:

numero = int(input("Digite aqui um numero: "))
print(numero)

antecessor = numero - 1
sucessor = numero + 1
print(f"O antecessor do numero digitado {numero} é: {antecessor} e o sucessor do numero digitado {numero} é: {sucessor}")
