# Desafio 007- Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre sua media:

nota1 = float(input("Digite aqui a sua primeira nota: "))
# print(nota1)
nota2 = float(input("Digite agora sua segunda nota: "))
# print(nota2)

media = (nota1 + nota2) / 2
print(f"Sua média escolar é: {media:.1f}")