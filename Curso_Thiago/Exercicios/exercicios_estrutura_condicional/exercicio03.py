# Ex.03- Escreva um programa que calcule a soma de três variáveis e imprima o resultado na tela:

a = float(input("Digite aqui um numero: "))
b = float(input("Digite aqui outro numero: "))
c = float(input("Digite aqui outro numero: "))

soma_final = a + b + c
print (f"A soma dos três valores digitados é: {soma_final}")
