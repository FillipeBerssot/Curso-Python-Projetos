# Ex.04- Calcule um aumento de 15% para um salário de R$ 750:

salario_inicial = 750
aumento_salario = (15 / 100)                                            #750 * (15/100)
aumento_salario1 = salario_inicial * aumento_salario
print(aumento_salario1)

salario_final = salario_inicial + aumento_salario1

print(f"O aumento de 15% em um salario de R$ 750 será R$ {aumento_salario1}, totalizando um salario final de R$ {salario_final}")